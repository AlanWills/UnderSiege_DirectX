#include "pch.h"
#include "BaseData.h"


//-----------------------------------------------------------------------------------------------------------------------------------
BaseData::BaseData() :
	m_document(nullptr)
{
}


//-----------------------------------------------------------------------------------------------------------------------------------
BaseData::~BaseData()
{
}


//-----------------------------------------------------------------------------------------------------------------------------------
void BaseData::LoadData(const char* filename)
{
	m_document.reset(new tinyxml2::XMLDocument());
	m_document->LoadFile(filename);

	assert(m_document);
}